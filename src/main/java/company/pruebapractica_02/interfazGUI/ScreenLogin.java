    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed

        String user = usuarioTextField.getText();
        char[] pass = passwordField.getPassword();
        String passParset = new String(pass);

        if (user.isEmpty() || passParset.isEmpty()) {
            JOptionPane.showMessageDialog(
                    null,
                    "los campos no pueden estar vacios\nIntémtelo de nuevo",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);

        } else if (!util.isEmailCorrect(user)) {
            JOptionPane.showMessageDialog(
                    null,
                    "El formato del email es incorrecto."
                    + "\n - Debe cumplir el siguiente formato: xxx@gmail.com",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
        } else if (util.isPassCorrect(passParset)) {
            JOptionPane.showMessageDialog(
                    null,
                    "La contraseña no cumple los requisitos."
                    + "\n - La contraseña es un token que debe tener 16 caractéres.",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
        } else {

            ScreenAdmin newScreenAdmin = new ScreenAdmin(user, passParset);
            this.dispose();
        }
    }//GEN-LAST:event_loginButtonActionPerformed

